    // Esta es una función que muestra datos en una matriz bidimencional que posteriormente se reflejan en la tabla
    private void Mostrar_datos(){
        String Matriz [][] = new String [List.size()][5];
        
        Asistente auxiliar;
        
        for (int i = 0; i < List.size(); i++) {
            auxiliar = List.get(i);
            Matriz [i][0] = auxiliar.getNombre();
            Matriz [i][1] = auxiliar.getCedula();
            Matriz [i][2] = Integer.toString(auxiliar.getedad());
            Matriz [i][3] = auxiliar.getsilla();
            Matriz [i][4] = auxiliar.getDia();
            
                    jTable_Asistentes.setModel(new javax.swing.table.DefaultTableModel(
Matriz,
            new String [] {
                "Nombre", "Cedula ", "Edad", "N° de Puesto", "Dia del Evento"
            }
        ));
        }
        
        
    }
    
    private void Agragar_a_tabla(){
        
        String Nombre;
        int edad;
        String silla;
        String Cedula;
        String Dia;
        
        
 try {

         Nombre = jTextField_Nombre.getText();
         Cedula = jTextField_Cedula.getText();
         edad = Integer.parseInt(jTextField_Edad.getText());
         silla = jComboBox_Puestos.getSelectedItem().toString();
         Dia = jComboBox_Dia.getSelectedItem().toString();
         
         List.add(new Asistente(Nombre, Cedula, edad, silla, Dia));
         
} catch (Exception e) {
     JOptionPane.showMessageDialog(null,"Verifica los datos");
        }
      
       Mostrar_datos();
        
    }
    
    private void jButton_AgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_AgregarActionPerformed

                boolean bandera = false;
        
            for(int i=0; i<List.size(); i++){
                if(jComboBox_Puestos.getSelectedItem().toString().equals(List.get(i).getsilla())
                        && jComboBox_Dia.getSelectedItem().toString().equals(List.get(i).getDia())){
                    bandera = true;
                   }                   
          }
            
            if(bandera == false){
                Agragar_a_tabla ();

            }else{
             JOptionPane.showMessageDialog(null,"El puesto ya se encuentra ocupado, selecciona otro");
            }
            
        Limpiar ();
    
    }//GEN-LAST:event_jButton_AgregarActionPerformed

