    // Esta es una función para guardar los datos de los asistentes
    private void Guardar (){
    
        File file = new File ("Asistentes.txt");
        PrintWriter Escribe;
        if (!file.exists()){
            try {
                file.createNewFile();
            } catch (Exception e) {
            }
        }
        
        try {
            Asistente auxiliar;
            Escribe = new PrintWriter(file,"utf-8");
            
            for (int i = 0; i < List.size(); i++) {
                auxiliar = List.get(i);
                auxiliar.Guardar(Escribe);
            }
            Escribe.close();
        } catch (Exception e) {
        }
    }
    
    
    // Con esta función se reflrezca los datos desde un txt que se genera automaticamente al guardarlos.
    public void Cargar(){
        
    File Archivo = new File ("Asistentes.txt");
        FileReader Leer;
        BufferedReader Almacen;
        
        Asistente persona, auxiliar = new Asistente();
        
        try {
            
            Leer = new FileReader(Archivo);
            Almacen = new BufferedReader(Leer);
            persona = auxiliar.Cargar(Almacen);
            
            while (persona != null){
            
                List.add(persona);
                persona = auxiliar.Cargar(Almacen);
            }
         Almacen.close();
         Leer.close();
            
        } catch (Exception e) {
        }
        Mostrar_datos();
    }
    
